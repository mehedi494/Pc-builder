export const getServerSideProps = async (context) => {
  // ?category=${category}
  const { category } = context.params;

  const res = await fetch(
    `${process.env.SERVER_BASE_URL}/products?category=${category}`
  );
  const data = await res.json();

  return {
    props: {
      data,
    },
  };
};
